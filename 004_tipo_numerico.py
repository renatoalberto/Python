"""
Tipo numérico
"""
print(f'{45}')
print(f'{7 + 3}')
print(f'{3 * 5}')

# divisão com resultado real e inteiro
print(f'{5 / 2}')
print(f'{int(5 / 2)}')
print(f'{5 // 2}')

# módulo - resto da divisão
print(f'{4 % 2}')
print(f'{7 % 2}')

# elevado - demonstrando ilimitação da linguagem
print(f'{3 ** 3}')
print(f'{2 ** 63}')
print(f'{2 ** 1000}')

# python permite separar casas por underline, facilita a visualização
print(f'{1_000_000_000_000}')

numero = 30
print(numero)
print(numero + 1)
numero += 1
print(numero)

print(type(numero))
